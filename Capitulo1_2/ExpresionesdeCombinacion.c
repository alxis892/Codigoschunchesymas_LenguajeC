#include <stdio.h>

int main() {
    int x = 10;
    int y = 3;
    int z = 5;
    float resultado;

    // Expresión combinada: una combinación de operaciones aritméticas y lógicas
    resultado = (x + y) * z / 2.0;

    // Impresión del resultado
    printf("El resultado de la expresión combinada es: %.2f\n", resultado);

    return 0;
}
