#include <stdio.h>

int main() {
    //declaramos las variables
    int x = 5;
    int b = 10;

    // Operador de incremento ++
    x++; // Incrementa x en 1
    b--; //decrementamos b en 1

    // Imprime el valor de x despu√©s del incremento
    printf("El valor del incremento x es: %d\n", x); // Resultado: 
    printf("El valor del incremento b es: %d\n", b); // Resultado:
    printf("La suma de las variables con operadores de asignacion es: %d\n", x+b); // Resultado:  
    return 0;
}
